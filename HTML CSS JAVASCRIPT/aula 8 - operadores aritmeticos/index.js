// Adição de dois valores
const a = 2;
const b = 3;
const resultado = a + b;

console.log(`${a} + ${b} = ${resultado}`); // 2 + 3 = 5

// Subtração de um valor de outro
const x = 7;
const y = 3;
const resultado = x - y;

console.log(`${x} - ${y} = ${resultado}`); // 7 - 3 = 4

// Multiplicação de dois valores
const num1 = 4;
const num2 = 5;
const resultado = num1 * num2;

console.log(`${num1} * ${num2} = ${resultado}`); // 4 * 5 = 20

// Divisão de um valor por outro
const dividend = 10;
const divisor = 2;
const resultado = dividend / divisor;

console.log(`${dividend} / ${divisor} = ${resultado}`); // 10 / 2 = 5

// Resto da divisão de um valor por outro
const num = 10;
const divisor = 3;
const resultado = num % divisor;

console.log(`${num} % ${divisor} = ${resultado}`); // 10 % 3 = 1
